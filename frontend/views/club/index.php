            'name',
            'address',
            'created_at',
            //'updated_at',
            //'updated_by',
//            'deleted_at',
            //'deleted_by',
            [
                'class' => ActionColumn::class,
                'urlCreator' => function ($action, Club $model, $key, $index, $column) {
                    return Url::toRoute([$action, 'id' => $model->id]);
                 }
            ],
        ],
    ]); ?>

    <?php Pjax::end(); ?>

